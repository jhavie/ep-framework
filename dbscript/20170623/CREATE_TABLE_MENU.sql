(MENU_CODE);


ALTER TABLE GCC.GCC_GDWS_SYS_MENU ADD (
  CONSTRAINT GCC_GDWS_SYS_MENU_PK
  PRIMARY KEY
  (ID)
  USING INDEX GCC.PK_GCC_GDWS_SYS_MENU
  ENABLE VALIDATE);

